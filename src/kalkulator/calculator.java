    private void samadenganbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_samadenganbtnActionPerformed
        b = Double.parseDouble(tampil.getText());
        if(op == "+"){
            result = a+b;
            tampil.setText(String.valueOf(result));
            angka = "";
        }else if(op == "*"){
            result = a*b;
            tampil.setText(String.valueOf(result));
            angka = "";
        }else if(op == "-"){
            result = a-b;
            tampil.setText(String.valueOf(result));
            angka = "";
        }else if(op == "/"){
            result = a/b;
            tampil.setText(String.valueOf(result));
            angka = "";
        }
    }//GEN-LAST:event_samadenganbtnActionPerformed

